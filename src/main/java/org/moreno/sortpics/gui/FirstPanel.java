    public void sortJList() {
        DefaultListModel<ImageFileData> newListModel = new DefaultListModel<>();
        ListModel<ImageFileData> listModel = this.lsFilesToProcess.getModel();
        List<ImageFileData> list = new ArrayList<>();
        for (int i = 0; i < listModel.getSize(); i++) {
            list.add(listModel.getElementAt(i));
        }
        Collections.sort(list);

        newListModel.addAll(list);
        this.lsFilesToProcess.setModel(newListModel);
    }

    public void attachContextMenu(JList<?> list) {
        list.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getButton() == MouseEvent.BUTTON3) { // Clic derecho
                    int index = list.locationToIndex(e.getPoint());
                    if (index != -1) {
                        list.setSelectedIndex(index);
                        JPopupMenu contextMenu = createContextMenu();
                        contextMenu.show(list, e.getX(), e.getY());
                    }
                }
            }
        });
    }

    private JPopupMenu createContextMenu() {
        JPopupMenu contextMenu = new JPopupMenu();
        JMenuItem miBackToOriginalName = new JMenuItem("Renombrar al nombre original");
        JMenuItem miRenameNewName = new JMenuItem("Editar nuevo nombre");
        JMenuItem removeFile = new JMenuItem("Borrar");

        // Agrega un ActionListener al miBackToOriginalName
        miBackToOriginalName.addActionListener(((e) -> {
            // Obtiene el elemento seleccionado en la lista
            ImageFileData selectedItem = lsFilesToProcess.getSelectedValue();
            if (selectedItem != null) {
                try {
                    selectedItem.moveToNoCameraStampName();
                    sortJList();
                } catch (IOException ex) {
                    // TODO manage error
                    Logger.getLogger(FirstPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }));

        miRenameNewName.addActionListener((e -> {
            // Obtiene el elemento seleccionado en la lista
            ImageFileData selectedItem = lsFilesToProcess.getSelectedValue();
            if (selectedItem != null) {
                var newName = showEditTextDialog(null, selectedItem.getNewName());
                selectedItem.setNewName(newName);
                sortJList();
            }
        }));

        removeFile.addActionListener((e) -> {
            try {
                ImageFileData selectedItem = lsFilesToProcess.getSelectedValue();
                selectedItem.remove();
            } catch (IOException ex) {
                // TODO manage error
                Logger.getLogger(FirstPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

        });
        contextMenu.add(miBackToOriginalName);
        contextMenu.add(miRenameNewName);
        contextMenu.add(removeFile);

        return contextMenu;
    }

