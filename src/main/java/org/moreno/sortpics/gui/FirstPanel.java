    public void updateJList(List<ImageFileData> newItem) {
        // Obtenemos el modelo actual del JList
        javax.swing.DefaultListModel<ImageFileData> model = (javax.swing.DefaultListModel<ImageFileData>) lsFilesToProcess.getModel();

        // Eliminamos todos los elementos existentes en el modelo
        model.removeAllElements();

        // Añadimos los nuevos elementos a partir de la lista proporcionada
        for (ImageFileData imageFileData : newItem) {
            model.addElement(imageFileData);
        }

        // Notificamos al JList que el modelo ha sido modificado (no siempre es necesario, pero es una buena práctica)
        lsFilesToProcess.setModel(model);
    }

